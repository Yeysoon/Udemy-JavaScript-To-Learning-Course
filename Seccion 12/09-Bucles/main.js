/*Bucle for = es una estructura de control que ejecuta un bloque de codigo varias veces de forma
automatica mientras que se cumpla una condicion

estructura:

for (inicializacion; condicion; incremento){
    codigo que se ejecutara mientras se cumpla la condicion
}

Ejemplo:


*/


let numero = 177;

for (let contador = 0; contador <= numero; contador++) {
    console.log(contador);
}